public class DoWhileLoop {

    public static void main(String[] args) {
         int n=0;   //Intialising
         do {
             System.out.println("Codebuster Testing");
             System.out.println(n);
             n++;   //Increment/Decrement
         } while (n<=5);    //Condition
        System.out.println("Java");
    }

}
